/**
 * A custom listener interface to create listeners from.
 * Uses the observer design pattern.
 * 
 * @author  Mohamed Haryz Izzudin bin Mohamed Rafy (1141127874)
 */
public interface CustomListener {
    /**
     * Performs an operation when the listener is updated of an event.
     * 
     * @author  Haryz
     */
    public void update();
}